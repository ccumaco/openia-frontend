<script lang="ts">
import { defineComponent} from 'vue';
import Toolbar from './components/Toolbar.vue'
import FooterGloblal from './components/Footer.vue'
export default defineComponent({
 components:{
  Toolbar,
  FooterGloblal
 },
  data(){
    return{
      loggedIn: false
    }
  },
  methods:{
    checkLogin() {
      // Verificar si el usuario est√° conectado
      if (localStorage.getItem('user')) {
        this.loggedIn = true
      } else {
        this.loggedIn = false
      }
    }
  },
  created() {
    this.checkLogin()
  }
})
</script>

<template>
  <Toolbar />
  <router-view></router-view>
  <FooterGloblal />
</template>

